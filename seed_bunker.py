# ══════════════════════════════════════════════════════════
#  SEED LOGIC
# ══════════════════════════════════════════════════════════

async def seed():
    async with aiosqlite.connect(DB_PATH) as db:
        db.row_factory = aiosqlite.Row

        # Ensure bunker tables exist
        await db.executescript("""
            CREATE TABLE IF NOT EXISTS bunker_attributes (
                id      INTEGER PRIMARY KEY AUTOINCREMENT,
                type    TEXT NOT NULL,
                text_uk TEXT NOT NULL,
                text_ru TEXT
            );
            CREATE TABLE IF NOT EXISTS bunker_scenarios (
                id           INTEGER PRIMARY KEY AUTOINCREMENT,
                type         TEXT NOT NULL,
                text_uk      TEXT NOT NULL,
                text_ru      TEXT,
                capacity_min INTEGER DEFAULT 2,
                capacity_max INTEGER DEFAULT 5
            );
        """)
        await db.commit()

        inserted = 0

        # ── Catastrophes ──
        for text_uk, text_ru in CATASTROPHES:
            row = await (await db.execute(
                "SELECT id FROM bunker_scenarios WHERE type='catastrophe' AND text_uk=?", (text_uk,)
            )).fetchone()
            if not row:
                await db.execute(
                    "INSERT INTO bunker_scenarios (type, text_uk, text_ru) VALUES ('catastrophe', ?, ?)",
                    (text_uk, text_ru),
                )
                inserted += 1

        # ── Bunkers ──
        for text_uk, text_ru, cap_min, cap_max in BUNKERS:
            row = await (await db.execute(
                "SELECT id FROM bunker_scenarios WHERE type='bunker' AND text_uk=?", (text_uk,)
            )).fetchone()
            if not row:
                await db.execute(
                    "INSERT INTO bunker_scenarios (type, text_uk, text_ru, capacity_min, capacity_max) VALUES ('bunker', ?, ?, ?, ?)",
                    (text_uk, text_ru, cap_min, cap_max),
                )
                inserted += 1

        # ── Attributes ──
        attr_sets = [
            ("age",        AGES),
            ("profession", PROFESSIONS),
            ("health",     HEALTH),
            ("hobby",      HOBBIES),
            ("phobia",     PHOBIAS),
            ("baggage",    BAGGAGE),
            ("ability",    ABILITIES),
        ]
        for attr_type, items in attr_sets:
            for text_uk, text_ru in items:
                row = await (await db.execute(
                    "SELECT id FROM bunker_attributes WHERE type=? AND text_uk=?", (attr_type, text_uk)
                )).fetchone()
                if not row:
                    await db.execute(
                        "INSERT INTO bunker_attributes (type, text_uk, text_ru) VALUES (?, ?, ?)",
                        (attr_type, text_uk, text_ru),
                    )
                    inserted += 1

        await db.commit()
        print(f"[OK] Bunker seed complete. Inserted {inserted} new records.")

        # Summary
        for tbl, col in [
            ("bunker_scenarios", "type='catastrophe'"),
            ("bunker_scenarios", "type='bunker'"),
            ("bunker_attributes", "type='profession'"),
            ("bunker_attributes", "type='health'"),
            ("bunker_attributes", "type='hobby'"),
            ("bunker_attributes", "type='phobia'"),
            ("bunker_attributes", "type='baggage'"),
            ("bunker_attributes", "type='ability'"),
        ]:
            row = await (await db.execute(f"SELECT COUNT(*) FROM {tbl} WHERE {col}")).fetchone()
            print(f"  {col}: {row[0]} records")


if __name__ == "__main__":
    asyncio.run(seed())
